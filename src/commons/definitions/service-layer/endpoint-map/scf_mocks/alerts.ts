  public getAlerts(
    page: number = 1,
    pageSize: number = 25,
  ): { results: Alert[]; currentPage: number; pageCount: number; pageSize: number; rowCount: number } {
    let cont = 0;
    let asd = [0, 1, 2, 3];
    let cont2 = 0;
    let aspgk = [1, 10, 20];
    this.debtors.map((elem: any) => {
      elem.alertAction = asd[cont];
      cont += 1;
      cont = cont > 3 ? (cont = 0) : cont;
      elem.priority = aspgk[cont2];
      cont2 += 1;
      cont2 = cont2 > 2 ? (cont2 = 0) : cont2;
    });
    return {
      results: this.debtors.slice((page - 1) * pageSize, page * pageSize),
      currentPage: page,
      pageCount: Math.floor(this.debtors.length / pageSize),
      pageSize: pageSize,
      rowCount: this.debtors.length,
    };
  }
